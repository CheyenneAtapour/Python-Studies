def height(root):
    count = 0
    l = [(root, 0)] # list of tuples: (node, height)
    while len(l) > 0:
        temp = l.pop()
        if not temp[0].left == None:
            l.append((temp[0].left, temp[1]+1))
            if temp[1] + 1 > count:
                count = temp[1] + 1
        if not temp[0].right == None:
            l.append((temp[0].right, temp[1]+1))
            if temp[1] + 1 > count:
                count = temp[1] + 1
    return count



